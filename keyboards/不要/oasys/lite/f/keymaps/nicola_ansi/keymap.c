bool oled_task_user(void) {

    if (oled == _OLED_INIT) {
        oledLogoTime = timer_read() + 5000;
        oled = _OLED_LOGO;
        return false;
    }
    if (oled == _OLED_LOGO) {
       if (timer_read() < oledLogoTime) {
            // Display Logo 5 sec
            render_logo();
        } else {
            oled_clear();
            oled = _OLED_STATE;
        }
        return false;
    }
    // Host Keyboard Layer Status
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            render_ime_off();
            break;
        case _NICOLA:
            render_nicola();
            break;
        case _FUNC:
            oled_write_P(PSTR("\n"), false);
            oled_write_P(PSTR("MO(2) _FUNC\n"), false);
            oled_write_P(PSTR("\n"), false);
            break;
        default:
            break;
    }

    if (is_capslock) {
        oled_set_cursor(0, 3);
        oled_write_P(PSTR("CAPSLOCK\n"), false);
    } else {
        oled_set_cursor(0, 3);
        oled_write_P(PSTR("\n"), false);
    }
    return false;
}



